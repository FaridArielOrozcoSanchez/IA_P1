'''
¿Qué es un paquete?
Un paquete es una carpeta que contiene módulos (.py) relacionados. Permite organizar el código en una estructura jerárquica y reutilizable.

¿Cómo se crea?
Crea una carpeta (ej. paquete)
Dentro, coloca tus módulos (ej. modulo1.py, modulo2.py)
Opcionalmente, añade __init__.py para inicializar el paquete

proyecto/
│
├── principal.py
└── paquete/
    ├── __init__.py
    ├── modulo1.py
    ├── modulo2.py
    └── subpaquete/
        ├── modulo3.py
        └── modulo4.py
En los archivos haya que descomprimir proyecto.zip para poder hacer que este codigo funcione
'''

