#Introducción a la programación modular con Python

'''
¿Qué es?
Es una técnica que consiste en dividir un programa grande en archivos más pequeños llamados módulos. 
Cada módulo contiene funciones, clases o variables relacionadas con una tarea específica.

¿Ventajas?
Mejora la legibilidad y organización del código
Facilita la depuración y el mantenimiento
Permite reutilizar código en otros proyectos
Reduce dependencias innecesarias
Aumenta el rendimiento al cargar solo lo necesario
'''

