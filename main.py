# Importamos el módulo calculadora
import calculadora

# Usamos las funciones del módulo
print("Suma:", calculadora.sumar(10, 5))         # Salida: 15
print("Resta:", calculadora.restar(10, 5))       # Salida: 5
print("Multiplicación:", calculadora.multiplicar(10, 5))  # Salida: 50
print("División:", calculadora.dividir(10, 0))   # Salida: Error: división entre cero
